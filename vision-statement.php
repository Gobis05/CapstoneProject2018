					<p><strong>President’s Vision of the future of the New Hampshire Off Highway Vehicle Association (NHOHVA) </strong></p>
<p><strong>What is a vision statement?</strong></p>
<p><strong>A vision statement</strong> is sometimes called a picture of your company in the future but it is so much more than that. Your vision statement is your inspiration, the framework for all your strategic planning. What we are doing when creating a vision statement is articulating our dreams and hopes for NHOHVA.   The vision statement answers the question, &#8220;Where do we want to go?&#8221;  Do not confuse a vision statement with a road map; it is not. What Bill Gates envisioned when he first started Microsoft was a personal computer in every home and business, <strong>not</strong> a series of steps for making that happen.</p>
<p>That is why it is important when crafting a vision statement to let our imagination go and dare to dream &#8211; and why it is important that a vision statement capture the organizations passion.</p>
<p>1st and foremost:  We should envision that NHOHVA represents all OHRV Consumers.  Whether it is a two wheeled, three wheeled, or a four-wheeled enthusiast, we represent and support them all.</p>
<p>2nd:  That we envision NHOHVA not as some bureaucratic organization that has its own agenda but as an organization of the OHRV community through its clubs – the lifeblood of OHRVing in New Hampshire.  We should not be doing anything that the clubs do such as events but rather should be supporting our member organizations in the areas that they cannot be as effective.  An example would be training club members on operating the NHOHVA safety trailer and then scheduling it for pick by the clubs for their use at club activities as a safety program – big or small.</p>
<p>3rd:  That everything that we do supports our clubs, their members, and the consumers in general.</p>
<p>4<sup>th</sup>:  That NHOHVA provides its clubs with services that they cannot afford nor have the resources to accomplishment without scarifying their real core commitment to their members – great trails and fun activities.</p>
<p>5<sup>th</sup>:  Create a significant working relationship with both Fish and Game and the Trails Bureau.  Examples of this would be coordinating Safety and Education Programs – not duplicating them; working together on legislative issues, taking into account the enforcement, trail, and consumer objectives, so that all parties move ahead in unison; a coordinated marketing effort to promote the sport; working together to update the Statewide plan; and working together so that the consumer can join a club, join NHOHVA, and register their machines at a one stop electronic shop by the 2015 season;</p>
<p>6<sup>th</sup>:  Create and maintain an electronic information system that informs and markets OHRV riding in New Hampshire.  Develop a website that would include an interactive map that would direct you to NHOHVA’s member clubs (possibly negating their own pages which is an expense now born by the clubs), a calendar listing and promoting all club activities;  an interactive map that would include touch screens that would take you to trail information, services, and whatever else you can imagine; NHOHVA handling all membership through the website helping to streamline the process and saving the clubs the administrative cost of processing memberships; and apps that would include interactive maps, etc.</p>
<p>In closing, we should be an organization that was created to be a larger voice for several subgroups that is created to do the things that they cannot do alone – the same reason that individuals join clubs – do the things that the clubs wish it to do and direct it to do so.   This is my vision – what is yours?</p>
<p>Harry C. Brown, President, NHOHVA</p>
